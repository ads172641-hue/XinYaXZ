const KNOWLEDGE_ENTRIES = [];

// 动态构建系统提示词（拼接知识库）
function buildSystemPrompt() {
  if (KNOWLEDGE_ENTRIES.length === 0) return SYSTEM_PROMPT;

  let knowledgeSection = '\n\n## 知识库参考\n以下是从知识库中检索到的相关信息，请在回答时结合参考：\n';
  KNOWLEDGE_ENTRIES.forEach(entry => {
    knowledgeSection += `\n### ${entry.title}\n${entry.content}\n`;
  });

  return SYSTEM_PROMPT + knowledgeSection;
}
